                        <div class="blog_details">
                            <p>Проекты разные, а ошибки, которые сводят на нет результаты рекламных кампаний - одинаковые. Возможно, моя выборка не репрезентативна. Но за 5 с хвостиком лет в маркетинге все, кто приходил на настройку, аудит или консультацию, совершают минимум одну из этих ошибок. В особо печальных случаях - все три.</p>
                            <p><strong>Ошибка 1 - Самолюбование</strong></p>
                            <p>Сайт или лендинг - это ода себе любимому. И все бы ничего. Но клиенты точно такие же эгоисты, как и владелец сайта. И у них на первом месте они сами и их конкретная проблема. Кто продавец и насколько он великолепен для покупателя второстепенно. Важно лишь одно - решить свою проблему с максимальной выгодой для себя. Все.</p>
                            <p><strong>Решение</strong>: как завещал Дейл Карнеги перепакуйте самовосхваление в выгоды для клиента. Вместо “Мы 12 лет на рынке” напишите “Проблему Х уже 12 лет успешно решает наш продукт”.</p>
                            <p><strong>Ошибка 2 - Делаем одно и тоже, ждем разный результат</strong></p>
                            <p>У этой ошибки есть две вариации.</p>
                            <p><em>Первая</em> - строим Воронку и делим семантику на целевые и околоцелевые запросы (горячие/холодные; продающие/информационные - в зависимости от того, кто у каких инфобизнесменов учился) и… Ведем всех на один и тот же лендинг. Да-да, на тот, где о себе прекрасном.</p>
                            <p><strong>Решение:</strong> даем посетителям сайта то, что обещано в рекламном объявлении. Если запрос был информационный, то страница должна содержать ответ на вопрос из него. Если продающий, то переводим пользователя на страницу с выгодным предложением, написанном по формуле Offer - Deadline - Call to action.</p>
                            <p><em>Вторая</em> - запускаем ремаркетинг на тех, кто уже был на сайте или лендинге и ведем их… Куда бы вы думали? Верно, на ту же посадочную страницу, которая в первый раз человека не вдохновила оставить заявку.</p>
                            <p><strong>Решение: </strong>запускать ремаркетинг только на специальное предложение с четким ограничением по времени или количеству.</p>
                            <p><strong>Ошибка 3 - Одноразовые продажи</strong></p>
                            <p>Какие бы успешные и красивые кейсы не презентовали специалисты, реальность такова, что стоимость заявок во всех рекламных каналах динамично растет. Поэтому подход к рекламе с позиции одноразовых продаж заведомо убыточный. Даже если рекламодателю повезло и по завершению кампании он вышел в ноль, но дальше никак эту базу не монетизирует. То в чем смысл? Получается, как в том анекдоте - мешок сажал, мешок копал - ничего не потерял.</p>
                            <p><strong>Решение:</strong> вести учет и сегментацию базы. Иметь минимум по 2-3 варианта, что дальше каждому сегменту можно предложить.</p>
