                        <h2>Ошибки в UX</h2>
                        <h3>6 ошибок юзабилити сайта, которые отпугивают посетителей</h3>
                        <p><strong>Юзабилити</strong> - один из главных факторов в продвижении сайта, который отвечает за удобство использования ресурса пользователями. На этот фактор пристально смотрят поисковые системы. К примеру, если сайт неудобный, поведение пользователей измениться. Когда человек заходит на ресурс, а он долго грузиться либо пользователь испытывает трудности в поиске необходимой информации, то зачастую сайт закрывают и ищут другой ресурс с подобным контентом. Такое поведение пользователей анализируется поисковиками, соответственно, роботы понижают сайт в рейтинге.</p>
                        <div class="image-box">
                            <img src="../images/articles/usability-bugs-img2.png" alt="image description">  
                        </div>                
                        <h3>Основные ошибки юзабилити:</h3>
                        <ul>
                            <li>
                                <strong>Устаревший дизайн сайта</strong> 
                                <p>Сегодня пользователи отдают предпочтение простым и лаконичным ресурсам. И если визуальная модернизация веб сайта проводилась еще в момент создания, то скорей всего пользователь посчитает сайт неудобным из-за своей сложности. К примеру, горизонтальная прокрутка.</p>
                                <p>Пользоваться таким инструментом неудобно, к тому же горизонтальная прокрутка - прошлый век. Сегодня существует адаптивный дизайн, который подстраивается под любые параметры экрана, не создавая трудностей в просмотре страницы.</p>
                            </li>
                            <li>
                                <strong>Стиль шрифта и его размер</strong> 
                                <p>Часто бывает, когда шрифт для пользователя выглядит нечитабельным либо размер контента слишком маленький.</p>
                            </li>
                            <li>
                                <strong>Страница долго загружается</strong> 
                                <p>Если сайт загружается 5 и более секунд, терпение пользователя не выдерживает, и он уходит из ресурса.</p>
                            </li>
                            <li>
                                <strong>Понятная навигация ресурса</strong> 
                                <p>Клиент на интуитивном уровне должен находить необходимый ему контент. Если клиент путается на сайте, он также закроет его и поищет более понятный в использовании ресурс, но уже на сайте конкурента.</p>
                            </li>
                            <li>
                                <strong>Слишком длинная регистрационная форма</strong> 
                                <p>Сегодня пользователь ценит свое время и ему не хочется тратить силы и время на заполнение длинной формы. Хотя он был готов сделать заказ, но количество полей для заполнения его пугают, и он ищет более легкий способ сделать заказ, но уже на другом сайте.</p>
                            </li>
                            <li>
                                <strong>При открытии сайта выскакивает окно на все поле сайта с просьбой о подписке</strong> 
                                <p>Человек еще не успев изучить ресурс, должен уже на что-то подписаться. Такие действия отпугивают пользователя, раздражаясь, человек моментально 	закрывает ресурс.</p>
                            </li>
                        </ul>
