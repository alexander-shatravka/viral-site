                            <h2>Редкая методика построения рекламных сообщений, когда AIDA не пригодна</h2>
                        </div>
                        <div class="blog_details">
                            <p>Все слышали о технологиях формирования рекламного посыла. На слуху обычно такие методики AIDA, ODS, Pain-More Pain-Hope-Solution, и др. но лишь однажды я слышал технологию "Потребности-Интересы-Ценности", и она имеет право на жизнь, потому что обладает рядом преимуществ.</p>
                            <p>Т.к. нигде я её описания не нашел, назову её формула NIV needs (потребности)- interests ( интересы ) values (ценности)</p>
                            <p>Суть заключается в том, что имея знания о сегменте широкой аудитории (в основном B2C), расписать её потребности, ценности и интересы, а потом через первое и второе бить в третье. Пока это сложно для понимания, но давайте по порядку, тут важно “отделить мух от колет”. Имеем три категории:</p>
                            <ul>
                                <li><strong>Потребность</strong> - нужда или недостаток в чем-либо, желание чего-либо или то, что образно необходимо конкретному человеку или группе лиц.</li>
                                <li><strong>Интересы</strong> - позитивное отношение, связанное с повышенным вниманием к некоему объекту (или процессу) </li>
                                <li><strong>Ценности</strong> - важность в данный момент, значимость, польза, полезность чего-либо. </li>
                            </ul>
                            <p>С теорией закончили, теперь надо расписать по целевой аудитории. Для примера возьмем студентов очников и короткими фразами накидываем несколько подходящих аудитории словосочетаний (ключей) по каждой категории. Потребности - развлечения, тусовка, секс, независимость, свобода, работа, общение, социальные сети. Интересы - гаджеты, новинки, современные тренды, дешевизна (или халява). Ценности - деньги, отношения, поддержка (или одобрение), развитие, положительная оценка (со стороны окружения).</p>
                            <p>Самое главное не путать категорию, в которую отнести ключ, т.к. в разных ситуациях он может попасть в любую из них, всегда держите в голове вашу ЦА и понимайте четкое различие между категориями.</p>
                            <p>Выпишем в 3 столбика то, что получилось (это удобно делать в excel) и начинаем комбинировать 1+2+3, по смыслу к объекту вашего предложения. Вот накидал для примера за час для разных сфер.</p>
